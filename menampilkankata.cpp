#include <iostream>

using namespace std;

int tampil(int n) {
	int i;
	i <= n;
	for (i = 1; i <= n; ++i)
		cout << "ALGORITMA DAN PEMROGRAMAN" << endl;
		return i;
}
int main() {
	int i, n, hasil;
	cout << "menampilkan ALGORITMA DAN PEMROGRAMAN" << endl;
	cout << "jumlah kata yang ingin ditampilkan: ";
	cin >> n;

	hasil = tampil(n);
	cout << hasil << endl;


	cin.get();
	return 0;

}
